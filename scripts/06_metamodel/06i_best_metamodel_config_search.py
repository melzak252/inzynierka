def main() -> None:
    """Run combined best-configuration search."""

    ASSETS_DIR.mkdir(parents=True, exist_ok=True)
    configure_plot_style()
    base = load_base_data()
    rolling = generate_rolling_features(CONTEXT_WINDOW)
    data = base.merge(rolling, on="golgg_match_id", how="inner").sort_values("date")

    feature_sets = {
        "optuna_player_uncertainty": OPTUNA_BASE_FEATURES,
        "optuna_player_core_context_w50": OPTUNA_BASE_FEATURES + ROLLING_CORE_FEATURES,
        "optuna_player_full_context_w50": OPTUNA_BASE_FEATURES + ROLLING_FULL_FEATURES,
    }
    update_intervals = [1000, 2000]
    mask_rates = [0.0, 0.1]

    rows: list[dict[str, object]] = []
    for feature_name, features in feature_sets.items():
        for update_interval in update_intervals:
            for mask_rate in mask_rates:
                variant = f"{feature_name} | upd={update_interval} | mask={mask_rate}"
                y_true, y_prob, dates = walk_forward_predict(data, features, update_interval, mask_rate)
                rows.append(
                    evaluate(
                        y_true,
                        y_prob,
                        dates,
                        variant,
                        feature_name,
                        update_interval,
                        mask_rate,
                        len(features),
                    )
                )

    results = pd.DataFrame(rows).sort_values("logloss").reset_index(drop=True)
    results.to_csv(ASSETS_DIR / "best_metamodel_config_results.csv", index=False)
    save_plot(results, "logloss", ASSETS_DIR / "best_metamodel_config_logloss.png")
    save_plot(results, "auc", ASSETS_DIR / "best_metamodel_config_auc.png")
    write_markdown_summary(results)

    print("\n=== BEST COMBINED METAMODEL SETTINGS ===")
    print(results.to_string(index=False))
    print("\nSaved artefacts to:", ASSETS_DIR)


if __name__ == "__main__":
    main()
