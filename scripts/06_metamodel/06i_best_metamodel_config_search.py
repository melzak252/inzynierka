"""Search combined best metamodel settings with walk-forward validation.

This script combines the best candidates discovered in previous experiments:

* optimized TrueSkill/OpenSkill predictions already present in ``golgg_y_predicts.csv``;
* Optuna LightGBM parameters from ``13_hyperparameter_optimization.py``;
* player-only feature preference;
* context window 50 as the best rolling-context candidate;
* update intervals 1000 and 2000;
* no masking versus light masking 0.1.

All evaluation rows are restricted to matches present in ``odds.csv``. Rolling
context is computed chronologically from full ``golgg_matches.json`` history.
"""

