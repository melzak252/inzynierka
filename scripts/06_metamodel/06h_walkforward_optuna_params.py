def load_odds_sample() -> pd.DataFrame:
    """Load rating predictions restricted to matches mapped in odds.csv.

    Returns:
        Chronologically sorted modelling frame filtered to 2020+ matches.
    """

    predictions = pd.read_csv(PROJECT_ROOT / "data" / "golgg_y_predicts.csv")
    odds = pd.read_csv(PROJECT_ROOT / "data" / "odds.csv", usecols=["golgg_match_id"])
    predictions["golgg_match_id"] = predictions["golgg_match_id"].astype(str)
    odds["golgg_match_id"] = odds["golgg_match_id"].astype(str)
    data = predictions.merge(odds.drop_duplicates(), on="golgg_match_id", how="inner")
    data["date"] = pd.to_datetime(data["date"])
    data = data.dropna(subset=FEATURES + [TARGET]).copy()
    data = data[data["date"] >= pd.Timestamp("2020-01-01")]
    return data.sort_values("date").reset_index(drop=True)


def walk_forward_predict(
    data: pd.DataFrame,
    params: dict[str, object],
    update_interval: int,
    mask_rate: float,
    random_seed: int = 42,
) -> tuple[np.ndarray, np.ndarray, pd.Series]:
    """Generate walk-forward predictions for one model configuration.

    Args:
        data: Chronologically sorted odds-mapped modelling data.
        params: LightGBM parameters.
        update_interval: Number of future matches between model refits.
        mask_rate: Probability of masking each training feature value.
        random_seed: Seed used for deterministic masking.

    Returns:
        Tuple of true labels, predicted probabilities, and prediction dates.
    """

    initial_train = data[data["date"] < pd.Timestamp("2021-01-01")].copy()
    test_pool = data[data["date"] >= pd.Timestamp("2021-01-01")].copy()
    if initial_train.empty or test_pool.empty:
        raise ValueError("Walk-forward split produced empty train or test set.")

    rng = np.random.default_rng(random_seed)
    train_df = initial_train.copy()
    y_true_parts: list[np.ndarray] = []
    prob_parts: list[np.ndarray] = []
    date_parts: list[pd.Series] = []

    for start in tqdm(range(0, len(test_pool), update_interval), desc="Walk-forward"):
        test_chunk = test_pool.iloc[start : start + update_interval]
        x_train = train_df[FEATURES].copy()
        if mask_rate > 0:
            x_train = x_train.mask(rng.random(x_train.shape) < mask_rate)

        model = LGBMClassifier(**params)
        model.fit(x_train, train_df[TARGET].astype(int))
        probabilities = model.predict_proba(test_chunk[FEATURES])[:, 1]

        y_true_parts.append(test_chunk[TARGET].astype(int).to_numpy())
        prob_parts.append(probabilities)
        date_parts.append(test_chunk["date"])
        train_df = pd.concat([train_df, test_chunk], ignore_index=True)

    return np.concatenate(y_true_parts), np.concatenate(prob_parts), pd.concat(date_parts, ignore_index=True)


def evaluate_predictions(
    y_true: np.ndarray,
    y_prob: np.ndarray,
    dates: pd.Series,
    variant: str,
    params_name: str,
    update_interval: int,
    mask_rate: float,
) -> dict[str, object]:
    """Calculate metrics for one walk-forward configuration."""

    clipped = np.clip(y_prob, 0.001, 0.999)
    return {
        "variant": variant,
        "params": params_name,
        "update_interval": update_interval,
        "mask_rate": mask_rate,
        "sample_size": len(y_true),
        "date_min": dates.min().date().isoformat(),
        "date_max": dates.max().date().isoformat(),
        "auc": roc_auc_score(y_true, clipped),
        "logloss": log_loss(y_true, clipped),
        "brier": brier_score_loss(y_true, clipped),
        "ece": calculate_ece(y_true, clipped),
        "accuracy_0_5": accuracy_score(y_true, clipped >= 0.5),
    }


def save_metric_plot(results: pd.DataFrame, metric: str, output_path: Path) -> None:
    """Save compact bar plot for a selected metric."""

    data = results.sort_values(metric, ascending=(metric == "logloss"))
    plt.figure(figsize=(12, 5.5))
    ax = sns.barplot(data=data, x="variant", y=metric, hue="params", palette="mako")
    for container in ax.containers:
        ax.bar_label(container, fmt="%.4f", fontsize=8, padding=3)
    values = data[metric].to_numpy()
    margin = max((values.max() - values.min()) * 0.25, 0.0015)
    ax.set_ylim(values.min() - margin, values.max() + margin)
    ax.set_title(f"Walk-forward Optuna vs default — {metric.upper()}", pad=15)
    ax.set_xlabel("")
    ax.set_ylabel(metric.upper())
    ax.tick_params(axis="x", rotation=20)
    sns.despine()
    plt.tight_layout()
    plt.savefig(output_path, bbox_inches="tight")
    plt.close()


