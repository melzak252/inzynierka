"""Validate Optuna LightGBM parameters with walk-forward evaluation.

This script is a follow-up to ``13_hyperparameter_optimization.py``. Optuna used
a chronological 80/20 holdout, so this script checks whether the selected
hyperparameters still look good under a more operational walk-forward protocol.
The evaluation is restricted to matches present in ``odds.csv``.
"""

