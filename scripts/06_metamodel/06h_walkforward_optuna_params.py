def main() -> None:
    """Run walk-forward validation and write artefacts."""

    ASSETS_DIR.mkdir(parents=True, exist_ok=True)
    configure_plot_style()
    data = load_odds_sample()

    experiments = [
        ("Default params, update 1000, no masking", "default", default_report_params(), 1000, 0.0),
        ("Optuna params, update 1000, no masking", "optuna", optuna_params(), 1000, 0.0),
        ("Default params, update 2000, no masking", "default", default_report_params(), 2000, 0.0),
        ("Optuna params, update 2000, no masking", "optuna", optuna_params(), 2000, 0.0),
        ("Optuna params, update 2000, masking 0.1", "optuna", optuna_params(), 2000, 0.1),
    ]

    rows: list[dict[str, object]] = []
    for variant, params_name, params, update_interval, mask_rate in experiments:
        y_true, y_prob, dates = walk_forward_predict(data, params, update_interval, mask_rate)
        rows.append(
            evaluate_predictions(
                y_true,
                y_prob,
                dates,
                variant,
                params_name,
                update_interval,
                mask_rate,
            )
        )

    results = pd.DataFrame(rows).sort_values("logloss").reset_index(drop=True)
    results.to_csv(ASSETS_DIR / "walkforward_optuna_results.csv", index=False)
    save_metric_plot(results, "logloss", ASSETS_DIR / "walkforward_optuna_logloss.png")
    save_metric_plot(results, "auc", ASSETS_DIR / "walkforward_optuna_auc.png")
    write_markdown_summary(results)

    print("\n=== WALK-FORWARD OPTUNA VALIDATION ===")
    print(results.to_string(index=False))
    print("\nSaved artefacts to:", ASSETS_DIR)


if __name__ == "__main__":
    main()
